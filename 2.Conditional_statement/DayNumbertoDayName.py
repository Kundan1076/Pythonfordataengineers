input_day_number=int(input("Enter Day number "))
if input_day_number ==0:
    print('Monday')
elif input_day_number ==1:
    print("Tuesday")
elif input_day_number ==2:
    print("Wednesday")
elif input_day_number ==3:
    print("Thursday")
elif input_day_number ==4:
    print("Friday")
elif input_day_number ==5:
    print("Saturday")
elif input_day_number ==6:
    print('Sunday')
else:
    print("enter a valid day")